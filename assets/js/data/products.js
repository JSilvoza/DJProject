/* ── Utility helpers ──────────────────────────────────────────── */

DG.getProductBySlug = (slug) =>
  DG.products.find(p => p.slug === slug) || null;

DG.getProductById = (id) =>
  DG.products.find(p => p.id === id) || null;

DG.getProductsByCategory = (category) =>
  category === 'all'
    ? DG.products
    : DG.products.filter(p => p.category === category);

DG.getNewArrivals = () =>
  DG.products.filter(p => p.isNew).slice(0, 8);

DG.getBestsellers = () =>
  DG.products.filter(p => p.isBestseller).slice(0, 8);

DG.getRelatedProducts = (product) =>
  (product.relatedIds || [])
    .map(id => DG.getProductById(id))
    .filter(Boolean);

DG.formatPrice = (cents) =>
  '$' + Number(cents).toFixed(0).replace(/\B(?=(\d{3})+(?!\d))/g, ',');

DG.getDisplayPrice = (product) =>
  product.salePrice || product.price;

DG.getSalePercent = (product) =>
  product.salePrice
    ? Math.round((1 - product.salePrice / product.price) * 100)
    : 0;

DG.renderStars = (rating) => {
  const full  = Math.floor(rating);
  const half  = rating % 1 >= 0.5 ? 1 : 0;
  const empty = 5 - full - half;
  return (
    '★'.repeat(full) +
    (half ? '½' : '') +
    '☆'.repeat(empty)
  );
};

